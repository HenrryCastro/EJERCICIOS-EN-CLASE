# Una tienda de frutas quiere registrar las frutas disponibles para la venta 

# Guardar en una lista cinco frutas diferentes 
# Muestre la lista completa de frutas 
# Muestre la únicamente la primera fruta de la lista
# Muestre únicamente la última fruta de la lista

frutas = ["manzana", "banana", "naranja", "pera", "uva"]
print("Frutas disponibles para la venta:")
for fruta in frutas:
    print(fruta)
print("\nLa primera fruta de la lista es:", frutas[0])
print("La última fruta de la lista es:", frutas[-1]) 