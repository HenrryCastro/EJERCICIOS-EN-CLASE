# Una estación meteorologica registro las temperaturas de una semana 

# 1. Guarda las temperaturas en una lista
# 2. Calcula el promedio de las temperaturas de la semana
# 3. Muestra el resultado


temperaturas = [28, 30, 29, 31, 27, 26, 30]
 
 
promedio = sum(temperaturas) / len(temperaturas)
 
 
print("El promedio de temperaturas es:", round(promedio, 2))

